void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance != TIM6)
		return;
	if(dirInc==1)
	{
		pulseWidth++;
		if(pulseWidth>=195)
		{
			pulseWidth=195;
			dirInc=0;
		}
	}
	else
	{
		pulseWidth--;
		if(pulseWidth<=5)
		{
			pulseWidth=5;
			dirInc=1;
		}
	}
	__HAL_TIM_SET_COMPARE(&htim14,TIM_CHANNEL_1,pulseWidth);
}
